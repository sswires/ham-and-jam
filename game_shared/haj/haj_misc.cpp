CRC32_t ComputeChecksum(const char* szBuffer)
{
	CRC32_t checksum;
	CRC32_Init(&checksum);
	CRC32_ProcessBuffer(&checksum, szBuffer, strlen(szBuffer));
	CRC32_Final(&checksum);
	return checksum;
}

/////////////////////////////////////////////////////////////////////////////
bool IntLessFunc_Ascending(const int& lhs, const int& rhs)
{
	return (lhs < rhs);
}

/////////////////////////////////////////////////////////////////////////////
bool IntLessFunc_Descending(const int& lhs, const int& rhs)
{
	return (lhs > rhs);
}

/////////////////////////////////////////////////////////////////////////////
bool FloatLessFunc_Ascending(const float& lhs, const float& rhs)
{
	return (lhs < rhs);
}

/////////////////////////////////////////////////////////////////////////////
bool FloatLessFunc_Descending(const float& lhs, const float& rhs)
{
	return (lhs > rhs);
}

